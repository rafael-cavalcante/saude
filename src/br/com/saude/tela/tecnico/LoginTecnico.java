    private void loginTecnico() {
        try {
            Tecnico tecnico = new Tecnico(
                    CPFService.validar(tf_cpf.getText()),
                    SenhaService.validar(new String(tf_senha.getPassword()))
            );

            tecnico = this.controllerTecnico.login(tecnico);

            if (tecnico != null) {
                MainTecnico mainTecnico = new MainTecnico(tecnico);
                mainTecnico.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "TECNICO N√ÉO ENCONTRADO");
            }
            limparCampos();
        } catch (Exception exception) {
            System.out.println(Cor.AMARELO.getCor() + exception.getMessage());
        }
    }

    private void limparCampos() {
        tf_cpf.setText("");
        tf_senha.setText("");
    }

